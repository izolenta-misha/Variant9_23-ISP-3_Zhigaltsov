/**HTTP :
HTTP-запрос состоит из:

Стартовая строка (метод, URI, версия протокола)

Заголовки (Host, User-Agent, Content-Type и др.)

Пустая строка (разделитель)

Тело запроса (необязательно, для POST/PUT)

Статус-коды HTTP:

        1xx: Информационные (100 Continue)

2xx: Успех (200 OK, 201 Created)

3xx: Перенаправление (301 Moved Permanently)

4xx: Ошибка клиента (404 Not Found)

5xx: Ошибка сервера (500 Internal Server Error)

GET vs POST:

GET: Получает данные, параметры в URL, кэшируется, ограниченная длина

POST: Отправляет данные, параметры в теле, не кэшируется, нет ограничений длины

HTTP работает поверх TCP, потому что:

TCP обеспечивает надежную доставку, необходимую для веб-страниц

HTTP использует потоковую модель TCP

Порты 80/443 для HTTP/HTTPS - стандартные порты TCP

TCP гарантирует порядок доставки пакетов

HTTP/1.1 улучшения:

Постоянные соединения (keep-alive по умолчанию)

Конвейерная обработка запросов

Поддержка виртуальных хостов (заголовок Host обязателен)

Улучшенное кэширование

Поддержка chunked transfer encoding*/