/**(DHCP нужен для автоматической настройки сетевых параметров. Без DHCP каждое устройство нужно настраивать вручную.

Процесс DORA:

Discover: Клиент ищет DHCP-сервер (broadcast)

Offer: Сервер предлагает IP-адрес

Request: Клиент запрашивает предложенный адрес

Acknowledge: Сервер подтверждает выдачу

Аренда IP-адреса - временное выделение адреса. Обновление через:

Обновление при достижении 50% времени аренды

Перезапрос при достижении 87.5%

Новый процесс DORA при истечении срока

Дополнительные параметры DHCP:

Маска подсети

Шлюз по умолчанию

DNS-серверы

Домен поиска

NTP-сервер

Два DHCP-сервера могут:

Выдавать конфликтующие IP-адреса

Создать путаницу в настройках

Привести к неработоспособности сети

Вызвать проблемы с безопасностью*/