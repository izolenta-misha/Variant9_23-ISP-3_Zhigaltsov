/**DNS:
DNS используется для преобразования доменных имен в IP-адреса. Без DNS пришлось бы запоминать числовые IP-адреса для каждого сайта.

Алгоритм DNS-запроса:

Проверка локального кэша

Запрос к рекурсивному DNS-серверу провайдера

Поиск от корневых серверов (. → .com → example.com)

Получение IP-адреса

Кэширование результата

Типы DNS-записей:

A: IPv4-адрес (32 бита)

AAAA: IPv6-адрес (128 битов)

CNAME: Псевдоним (каноническое имя)

Рекурсивный DNS-сервер:

Принимает запросы от клиентов

Сам выполняет все необходимые запросы к другим серверам

Возвращает конечный результат клиенту

Кэширует результаты для ускорения

DNS-кэш:

Хранит результаты предыдущих запросов

Ускоряет повторные обращения к тем же доменам

Уменьшает нагрузку на DNS-инфраструктуру

Снижает время отклика*/