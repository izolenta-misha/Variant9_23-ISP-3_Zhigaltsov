/**TCP
Трёхстороннее рукопожатие TCP - это процесс установления соединения:

Шаг 1 (SYN): Клиент отправляет серверу пакет с флагом SYN и случайным sequence number

Шаг 2 (SYN-ACK): Сервер отвечает пакетом с флагами SYN и ACK, подтверждает получение (ACK=seq+1) и отправляет свой sequence number

Шаг 3 (ACK): Клиент подтверждает получение ответа сервера (ACK=seq+1)

Роль: Гарантирует, что обе стороны готовы к передаче и знают начальные sequence numbers

Sequence number - это 32-битное число, которое:

Идентифицирует порядок байтов в потоке данных

Начинается со случайного значения при установке соединения

Увеличивается на количество переданных байтов

Используется для обеспечения правильной последовательности и надежности

TCP - потоковый протокол, потому что:

Передает данные как непрерывный поток байтов без границ сообщений

Не сохраняет границы между отправленными блоками данных

Приложение само должно определять границы сообщений

В отличие от UDP, который передает отдельные датаграммы с четкими границами

Надёжная доставка TCP обеспечивается через:

Подтверждения (ACK) для каждого полученного сегмента

Таймауты и повторные передачи при потере пакетов

Контрольные суммы для обнаружения ошибок

Управление потоком (окно перегрузки и скользящее окно)

Различия TCP и UDP:

TCP: с установкой соединения, надежный, с управлением потоком, гарантирует порядок

UDP: без соединения, ненадежный, без гарантий порядка

 TCP медленнее, потому что: устанавливает соединение, ждет подтверждений, управляет потоком, повторно передает потерянные пакеты
 */