/**UDP
UDP называют ненадёжным, потому что:

Не гарантирует доставку пакетов

Не подтверждает получение

Не повторяет отправку при потере

Не гарантирует порядок доставки

Датаграмма vs TCP-сегмент:

Датаграмма: независимый пакет с полным заголовком, содержит всю информацию для маршрутизации

TCP-сегмент: часть потока данных, зависит от других сегментов, имеет sequence numbers и подтверждения

UDP предпочтительнее TCP в:

VoIP и видеоконференциях (лучше потерять пакет, чем ждать повторной отправки)

DNS-запросах (быстрые, короткие запросы)

Онлайн-играх (низкая задержка важнее надежности)

Трансляции видео (потеря отдельных кадров не критична)

UDP не устанавливает соединение, потому что:

Проектировался для простоты и скорости

Каждая датаграмма самодостаточна

Не нужно управлять состоянием соединения

Меньше накладных расходов

Приложение может справляться с потерей пакетов:

Добавляя собственную логику подтверждения

Используя избыточное кодирование (FEC)

Применяя буферизацию и интерполяцию (для мультимедиа)

Отправляя критичные данные несколько раз
 */